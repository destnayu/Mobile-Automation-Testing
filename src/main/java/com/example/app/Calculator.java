package com.example.app;

public class Calculator {
    public static int add(int a, int b) {
        return a + b;
    }
    public static int min(int a, int b) {
        return a - b;
    }
    public static int div(int a, int b) {
        return a / b;
    }
    public static int multi(int a, int b) {
        return a * b;
    }
}